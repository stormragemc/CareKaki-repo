const DICTS: Record<Lang, Dict> = { en, zh, ms };

function lookup(dict: Dict, path: string): string | string[] | undefined {
  let node: Dict | string | string[] | undefined = dict;
  for (const part of path.split(".")) {
    if (node == null || typeof node === "string" || Array.isArray(node)) return undefined;
    node = node[part];
  }
  return typeof node === "string" || Array.isArray(node) ? node : undefined;
}

/** Translate a dotted key, with {placeholder} substitution. Falls back to English. */
export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const raw = lookup(DICTS[lang], key) ?? lookup(en, key) ?? key;
  let text = Array.isArray(raw) ? raw.join(" ") : raw;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replaceAll(`{${k}}`, String(v));
    }
  }
  return text;
}

/** Translate a dotted key that holds a string array (e.g. suggestion chips). */
export function translateList(lang: Lang, key: string): string[] {
  const raw = lookup(DICTS[lang], key) ?? lookup(en, key);
  return Array.isArray(raw) ? raw : [];
}
