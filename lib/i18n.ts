export const LANG_STORAGE_KEY = "careLang";

export function currentLang(): Lang {
  if (typeof window === "undefined") return "en";
  const v = window.localStorage.getItem(LANG_STORAGE_KEY);
  return v === "zh" || v === "ms" ? v : "en";
}

